/*
push - takes an element and add it to the last of the existing array
pop - remove last element from an array and returns it
shift - remove the first element of the array
unshift - add an element to the beginning of the array
*/

const arr = [1, 2, 3, 5, 6];

arr.pop()
arr.pop()
arr.push(4,5,6)
arr.unshift(0)